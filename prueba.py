
class Prueba:

    def Prueba(self,funcionalidad=None,objetivo=None):

        self.funcionalidad = funcionalidad
        self.objetivo = objetivo


    def getFuncionalidad(self):

        return self.funcionalidad

    def setFuncionalidad(self,pFuncionalidad):

        self.funcionalidad = pFuncionalidad

    def getObjetivo(self):

        return self.objetivo

    def setObjetivo(self,pObjetivo):

        self.objetivo = pObjetivo

    
